// Introductions Variables

String sum() {
  var n1 = 10;
  return '$n1 + $n1 = ${n1 + n1}';
}

String sub() {
  var n1 = 10;
  return '$n1 - $n1 = ${n1 - n1}';
}

String mult() {
  var n1 = 10;
  return '${n1} * ${n1} = ${n1 * n1}';
}

String div() {
  var n1 = 10;
  return '${n1} / ${n1} = ${n1 / n1}';
}
